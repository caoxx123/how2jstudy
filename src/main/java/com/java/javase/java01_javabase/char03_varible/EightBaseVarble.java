package com.java.javase.java01_javabase.char03_varible;

public class EightBaseVarble {
    public static void main(String[] args) {
        //四种整型
        byte i=1;
        short i1=1;
        int i2=1;
        long i3=1;
        //一种字符型
        char a='a';
        //两种浮点型
        float f1=0.1f;
        double  f2=1f;
        //布尔型
        boolean b1=true;
        System.out.println(b1);
    }
}
