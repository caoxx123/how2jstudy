package com.java.javase.java01_javabase.char04_operator;
//测试数字运算符
public class TestMathOperator {
    //数字运算符
    public static void main(String[] args) {
        int i=10;
        int j=10;
        System.out.println(i*j);
        System.out.println(i+j);
        System.out.println(i/j);
        System.out.println(i-j);
        System.out.println(i++);
        System.out.println(++i);
        System.out.println(j--);
        System.out.println(--j);
    }
}
